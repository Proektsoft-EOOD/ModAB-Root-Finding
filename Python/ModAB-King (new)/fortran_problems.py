# ---- cases 26:27  x e^(-1/x^2)  (0 at x=0)
def f_2627(x):
    # Fortran: x/exp(1/x^2). For tiny |x|, 1/x^2 overflows in Python's
    # math.exp (range error) whereas Fortran underflows the quotient to 0.
    # Reproduce the Fortran limiting behaviour exactly (x/exp(huge) -> 0).
    if x == 0.0:
        return 0.0
    try:
        return x/math.exp(1.0/(x*x))
    except OverflowError:
        return 0.0
